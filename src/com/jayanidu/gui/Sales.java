    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        // TODO add your handling code here:
        try {
            String enteredQty = jTextField5.getText();

            if (enteredQty.equals("")) {
                jLabel15.setText("" + qty);
            } else if (Integer.parseInt(enteredQty) <= qty) {
                jLabel15.setText("" + (qty - Integer.parseInt(enteredQty)));
            } else if (Integer.parseInt(enteredQty) > qty) {
                jTextField5.setText("");
                jLabel15.setText("" + qty);
            }

            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                int stockId = Integer.parseInt(jTextField2.getText().trim());

                boolean isFound = false;
                int row = -1;

                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    int stockIdTable = Integer.parseInt(jTable1.getValueAt(i, 0).toString());

                    if (stockId == stockIdTable) {
                        isFound = true;
                        row = i;
                        break;
                    }
                }

                if (isFound) {

                    int qtyTable = Integer.parseInt(jTable1.getValueAt(row, 5).toString());
                    Double totalTable = Double.parseDouble(jTable1.getValueAt(row, 7).toString());
                    jTable1.setValueAt(Integer.parseInt(enteredQty) + qtyTable, row, 5);

                    ResultSet rs = MySQL.search("SELECT * FROM `stock` INNER JOIN `product` ON `stock`.`product_id` = `product`.`id` INNER JOIN `brand` ON `product`.`brand_id` = `brand`.`id` INNER JOIN `type` ON `product`.`type_id` = `type`.`id` WHERE `stock`.`id` = '" + stockId + "'");
                    rs.next();
                    double total = Integer.parseInt(enteredQty) * rs.getInt("stock.sprice");
                    jTable1.setValueAt(total + totalTable, row, 7);

                    reset();
                    calculateNetTotal();
                    jTextField2.grabFocus();

                } else {

                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    ResultSet rs = MySQL.search("SELECT * FROM `stock` INNER JOIN `product` ON `stock`.`product_id` = `product`.`id` INNER JOIN `brand` ON `product`.`brand_id` = `brand`.`id` INNER JOIN `type` ON `product`.`type_id` = `type`.`id` WHERE `stock`.`id` = '" + stockId + "'");
                    rs.next();
                    Vector v = new Vector();
                    v.add(stockId);
                    v.add(jTextField3.getText().trim());
                    v.add(jTextArea2.getText().trim());
                    v.add(rs.getString("type.name"));
                    v.add(rs.getString("stock.sprice"));
                    v.add(enteredQty);
                    v.add(rs.getString("stock.exd"));

                    double total = Integer.parseInt(enteredQty) * rs.getInt("stock.sprice");
                    v.add(total);
                    dtm.addRow(v);

                    jTable1.setModel(dtm);
                    reset();
                    jTextField2.grabFocus();

                    calculateNetTotal();
                }
            }
        } catch (NumberFormatException e) {
            e.printStackTrace();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField5KeyReleased

