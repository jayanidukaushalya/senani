    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        String stockId = jTextField2.getText().trim();

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `stock` INNER JOIN `product` ON `stock`.`product_id` = `product`.`id` INNER JOIN `brand` ON `product`.`brand_id` = `brand`.`id` WHERE `stock`.`id` = '" + stockId + "'");
                if (rs.next()) {
                    jTextField3.setText(rs.getString("brand.name"));
                    jTextArea2.setText(rs.getString("product.description"));
                    int qtyStock = Integer.parseInt(rs.getString("stock.qty"));
                    jTextField5.setEditable(true);

                    int qtyPlus = 0;

                    for (int i = 0; i < jTable1.getRowCount(); i++) {
                        int stockIdTable = Integer.parseInt(jTable1.getValueAt(i, 0).toString());
                        if (Integer.parseInt(stockId) == stockIdTable) {
                            int qtyTable = Integer.parseInt(jTable1.getValueAt(i, 5).toString());
                            qtyPlus = qtyPlus + qtyTable;
                        }
                    }

                    qty = qtyStock - qtyPlus;
                    jLabel15.setText(qty + "");

                    jTextField5.grabFocus();
                    jTextField2.setEditable(false);
                }
            } catch (NumberFormatException e) {
                e.printStackTrace();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTextField2KeyReleased

