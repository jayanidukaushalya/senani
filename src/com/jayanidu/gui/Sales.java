    private void calculateNetTotal() {

        double total = 0;
        jLabel8.setText("0.00");

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            total = total + Double.parseDouble(jTable1.getValueAt(i, 7).toString());
            jLabel8.setText(new DecimalFormat("0.00").format(total));

        }

    }

    private void loadPaymentMethods() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `pmethod` WHERE `name` <> 'Cheque'");
            Vector v = new Vector();
            v.add("Select Method");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel cbm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(cbm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

