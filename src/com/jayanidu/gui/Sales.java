    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        long iid = Long.parseLong(jLabel2.getText());
        double netTotal = Double.parseDouble(jLabel8.getText());
        String payment = jTextField4.getText().trim();
        String pMethod = jComboBox1.getSelectedItem().toString();
        double balance = Double.parseDouble(jLabel13.getText());
        String dateTime = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No added any item to print!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (pMethod.equals("Select Method")) {
            JOptionPane.showMessageDialog(this, "Please select a payment method!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(0)|([1-9][0-9]*)|([0][.]([0]*[1-9][0-9]*))|([1-9][0-9]*[.]([0]*[1-9][0-9]*))").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid payment value!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                MySQL.iud("INSERT INTO `sales`(`id`,`date_time`,`nettotal`) VALUES('" + iid + "','" + dateTime + "'," + netTotal + ")");

                ResultSet rs1 = MySQL.search("SELECT * FROM `sales` WHERE `date_time` = '" + dateTime + "'");
                rs1.next();
                long id = rs1.getLong("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `pmethod` WHERE `name` = '" + pMethod + "'");
                rs2.next();
                int pMethodId = rs2.getInt("id");

                MySQL.iud("INSERT INTO `payment_sales`(`sales_id`, `pmethod_id`, `payment`, `balance`, `user_id`) VALUES(" + id + ", " + pMethodId + ", " + Double.parseDouble(payment) + ", " + balance + ", " + UserLogin.getUid() + ")");

                for (int i = 0; i < jTable1.getRowCount(); i++) {

                    int stockId = Integer.parseInt(jTable1.getValueAt(i, 0).toString());
                    int qtySell = Integer.parseInt(jTable1.getValueAt(i, 5).toString());
                    double total = Double.parseDouble(jTable1.getValueAt(i, 7).toString());

                    ResultSet rs3 = MySQL.search("SELECT * FROM `stock` WHERE `id` = " + stockId + "");
                    if (rs3.next()) {

                        int stockQty = rs3.getInt("qty");

                        int updateQty = stockQty - qtySell;
                        MySQL.iud("UPDATE `stock` SET `qty` = " + updateQty + " WHERE `id` = " + stockId + "");

                    }

                    MySQL.iud("INSERT INTO `sales_item`(`qty`, `sales_id`, `stock_id`, `total`) VALUES(" + qtySell + ", " + id + ", " + stockId + ", " + total + ")");

                }

                JOptionPane.showMessageDialog(this, "New Invoice created!", "Success", JOptionPane.INFORMATION_MESSAGE);

                reset();

                jLabel8.setText("0.00");
                jLabel13.setText("0.00");
                jTextField4.setText("");
                jTextField4.setEditable(false);
                jComboBox1.setSelectedIndex(0);
                generateInvoiceId();

                InputStream stream = getClass().getResourceAsStream("/com/jayanidu/reports/InvoiceSenani.jasper");

                HashMap parameteres = new HashMap();

                parameteres.put("Parameter1", dateTime);
                parameteres.put("Parameter3", String.valueOf(id));

                parameteres.put("Parameter7", String.valueOf(netTotal));
                parameteres.put("Parameter8", pMethod);
                parameteres.put("Parameter9", String.valueOf(Double.parseDouble(payment)));
                parameteres.put("Parameter10", String.valueOf(balance));

                JRTableModelDataSource dataSource = new JRTableModelDataSource(jTable1.getModel());
                JasperPrint jp = JasperFillManager.fillReport(stream, parameteres, dataSource);
                JasperViewer.viewReport(jp, false);
                JasperPrintManager.printReport(jp, true);

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton4ActionPerformed

