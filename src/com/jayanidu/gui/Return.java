    public void loadTable1() {
        try {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MySQL.search("SELECT c.`name`, br.`name`, s.`id`, s.`exd`, b.`name`, p.`description` FROM `stock` s INNER JOIN `product` p ON s.`product_id` = p.`id` INNER JOIN `brand` b ON p.`brand_id` = b.`id` INNER JOIN `grn_item` gi ON s.`id` = gi.`stock_id` INNER JOIN `grn` g ON gi.`grn_id` = g.`id` INNER JOIN `sales_rep` sp ON g.`sales_rep_id` = sp.`id` INNER JOIN `branch` br ON sp.`branch_id` = br.`id` INNER JOIN `company` c ON br.`company_id` = c.`id` WHERE sp.`id` = " + repId + " ORDER BY c.`name`");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("c.name"));
                v.add(rs.getString("br.name"));
                v.add(rs.getString("s.id"));
                v.add(rs.getString("b.name"));
                v.add(rs.getString("p.description"));
                v.add(rs.getString("s.exd"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

