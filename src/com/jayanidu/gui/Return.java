    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row != -1) {

            int stockId = Integer.parseInt(jTable1.getValueAt(row, 2).toString());

            try {

                DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();

                boolean isFound = false;

                for (int i = 0; i < dtm.getRowCount(); i++) {
                    int tableid = Integer.parseInt(jTable2.getValueAt(i, 0).toString());

                    if (tableid == stockId) {
                        isFound = true;
                        break;
                    }
                }

                if (isFound) {
                    JOptionPane.showMessageDialog(this, "Already added this product!", "Messeage", JOptionPane.INFORMATION_MESSAGE);
                } else {

                    ResultSet rs = MySQL.search("SELECT * FROM `stock` s INNER JOIN `grn_item` gi ON s.`id` = gi.`stock_id` WHERE s.`id` = " + stockId + "");
                    if (rs.next()) {
                        Vector v = new Vector();
                        v.add(rs.getString("s.id"));
                        v.add(rs.getString("s.qty"));
                        v.add(rs.getString("gi.bprice"));
                        dtm.addRow(v);
                    }

                    jTable2.setModel(dtm);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable2PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jTable2PropertyChange
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();

        if (row != -1) {
            try {

                int avbQty = Integer.parseInt(jTable2.getValueAt(row, 1).toString());
                double bprice = Double.parseDouble(jTable2.getValueAt(row, 2).toString());
                int qtyReturn = Integer.parseInt(jTable2.getValueAt(row, 3).toString());

                if (avbQty >= qtyReturn) {
                    double total = bprice * qtyReturn;
                    jTable2.setValueAt(total, row, 4);

                    updateTotal();
                } else {

                    jLabel2.setText("");
                    jTable2.setValueAt("", row, 3);
                    jTable2.setValueAt("", row, 4);
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jTable2PropertyChange

