    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        double value = Double.parseDouble(jLabel2.getText());

        if (repId == 0) {
            JOptionPane.showMessageDialog(this, "Please select a agent!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTable2.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No added any item to return!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (value == 0) {
            JOptionPane.showMessageDialog(this, "Please insert return qty using Table 2!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                Date d = new Date();
                String datetime = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(d);

                MySQL.iud("INSERT INTO `return`(`sales_rep_id`, `value`, `date_time`, `user_id`) VALUES(" + repId + ", " + value + ", '" + datetime + "', 3)");

                ResultSet rs = MySQL.search("SELECT `id` FROM `return` WHERE `date_time` = '" + datetime + "'");
                if (rs.next()) {
                    int returnId = rs.getInt("id");

                    for (int i = 0; i < jTable2.getRowCount(); i++) {

                        int stockId = Integer.parseInt(jTable2.getValueAt(i, 0).toString());
                        int avbQty = Integer.parseInt(jTable2.getValueAt(i, 1).toString());
                        int returnQty = Integer.parseInt(jTable2.getValueAt(i, 3).toString());

                        MySQL.iud("UPDATE `stock` SET `qty` = " + (avbQty - returnQty) + " WHERE `id` = " + stockId + "");
                        MySQL.iud("INSERT INTO `return_item`(`return_id`, `stock_id`, `qty`) VALUES(" + returnId + ", " + stockId + ", " + returnQty + ")");
                    }

                    JOptionPane.showMessageDialog(this, "Return successful!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    //Jasper Report
                    InputStream stream = getClass().getResourceAsStream("/com/jayanidu/reports/ReturnS.jasper");

                    String dateTime = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(d);

                    HashMap parameteres = new HashMap();

                    parameteres.put("Parameter1", dateTime);
                    parameteres.put("Parameter3", returnId);

                    ResultSet rs2 = MySQL.search("SELECT * FROM `sales_rep` sr INNER JOIN `branch` b ON sr.`branch_id` = b.`id` INNER JOIN `company` c ON b.`company_id` = c.`id` INNER JOIN `city` ct ON b.`city_id` = ct.`id` WHERE sr.`id` = " + repId + "");
                    if (rs2.next()) {
                        parameteres.put("Parameter2", rs2.getString("c.name"));
                        parameteres.put("Parameter4", rs2.getString("b.name"));
                        parameteres.put("Parameter5", rs2.getString("b.line1") + " " + rs2.getString("b.line2"));
                        parameteres.put("Parameter6", rs2.getString("ct.name"));
                        parameteres.put("Parameter7", jLabel2.getText());
                    }

                    Vector v = new Vector();

                    for (int i = 0; i < jTable2.getRowCount(); i++) {
                        
                        ResultSet rs3 = MySQL.search("SELECT * FROM `stock` s INNER JOIN `product` p ON s.`product_id` = p.`id` INNER JOIN `brand` b ON p.`brand_id` = b.`id` WHERE s.`id` = '" + jTable2.getValueAt(i, 0).toString() + "'");
                        rs3.next();

                        v.add(new ReturnBean(Double.parseDouble(jTable2.getValueAt(i, 2).toString()), Integer.parseInt(jTable2.getValueAt(i, 3).toString()), Double.parseDouble(jTable2.getValueAt(i, 4).toString()), rs3.getString("b.name"), rs3.getString("p.description"), rs3.getString("s.exd")));
                    }

                    JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(v);

                    JasperPrint jp = JasperFillManager.fillReport(stream, parameteres, dataSource);
                    JasperViewer.viewReport(jp, false);
                    JasperPrintManager.printReport(jp, true);

                    clearFields();

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

