    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String cname = jTextField1.getText().trim();
        String cnum = jTextField2.getText().trim();

        if (cname.isBlank()) {
            JOptionPane.showMessageDialog(this, "Company Name field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cname.length() > 45) {
            JOptionPane.showMessageDialog(this, "Company Name field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cnum.isBlank()) {
            JOptionPane.showMessageDialog(this, "Contact Number field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cnum.length() > 45) {
            JOptionPane.showMessageDialog(this, "Contact Number field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECt * FROM `company` WHERE `name` = '" + cname + "' AND `phone` = '" + cnum + "' AND `status` = 1");
                if (!rs.next()) {

                    MySQL.iud("INSERT INTO `company`(`name`, `phone`) VALUES('" + cname + "', '" + cnum + "')");
                    JOptionPane.showMessageDialog(this, "Company saved successfull!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    jTextField1.setText("");
                    jTextField2.setText("");

                    loadTable();
                    b.loadCompanies();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    String clickedCname;
    String clickedCnum;

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) {
            int row = jTable1.getSelectedRow();
            if (row != -1) {

                clickedCname = jTable1.getValueAt(row, 0).toString();
                clickedCnum = jTable1.getValueAt(row, 1).toString();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        if (row != -1) {
            int showConfirmDialog = JOptionPane.showConfirmDialog(this, "Do you really want to remove this selected Company?", "Remove", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (showConfirmDialog == JOptionPane.YES_OPTION) {

                MySQL.iud("UPDATE `company` SET `status` = 0 WHERE `name` = '" + clickedCname + "'");
                loadTable();
                jTextField1.setText("");
                jTextField2.setText("");
                
                b.loadCompanies();
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

