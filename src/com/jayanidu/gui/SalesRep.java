    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String cname = jTextField6.getText();
        String bname = jTextField5.getText();
        String name = jTextField4.getText().trim();
        String mobile = jTextField2.getText().trim();

        if (cname.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please select a branch!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Name field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (name.length() > 45) {
            JOptionPane.showMessageDialog(this, "Name field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isBlank()) {
            JOptionPane.showMessageDialog(this, "Mobile field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.length() > 10) {
            JOptionPane.showMessageDialog(this, "Mobile field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet rs1 = MySQL.search("SELECT * FROM `branch` INNER JOIN `company` ON `branch`.`company_id` = `company`.`id` WHERE `branch`.`name` = '" + bname + "' AND `company`.`name` = '" + cname + "' AND `branch`.`status` = 1");
                rs1.next();
                int bId = rs1.getInt("id");

                ResultSet rs = MySQL.search("SELECT * FROM `sales_rep` WHERE `branch_id` = " + bId + " AND `name` = '" + name + "' AND `phone` = '" + mobile + "' AND `status` = 1");
                if (!rs.next()) {

                    MySQL.iud("INSERT INTO `sales_rep`(`branch_id`,`name`,`phone`,`status`) VALUES(" + bId + ",'" + name + "','" + mobile + "',1)");
                    JOptionPane.showMessageDialog(this, "Agent added successfull!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    clearFields();
                    loadTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Agent already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

