    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        int row = jTable1.getSelectedRow();

        if (row != -1) {
            try {

                ResultSet rs = MySQL.search("SELECT * FROM `sales_rep` WHERE `name` = '" + jTable1.getValueAt(row, 2).toString() + "' AND `status` = 1");
                rs.next();

                clickedID = rs.getInt("id");
                clickedBranch = rs.getInt("branch_id");

                if (evt.getClickCount() == 1) {

                    clickedName = jTable1.getValueAt(row, 2).toString();
                    clickedMobile = jTable1.getValueAt(row, 3).toString();

                    jTextField6.setText(jTable1.getValueAt(row, 0).toString());
                    jTextField5.setText(jTable1.getValueAt(row, 1).toString());
                    jTextField2.setText(clickedMobile);
                    jTextField4.setText(clickedName);

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

        if (evt.getClickCount() == 2) {
            if (g != null) {
                g.jTextField8.setText(clickedName);
                g.jTextField9.setText(clickedMobile);
                g.repId = clickedID;
                this.dispose();
            }

            if (r != null) {
                r.jTextField8.setText(clickedName);
                r.jTextField9.setText(clickedMobile);
                r.repId = clickedID;
                r.loadTable1();
                this.dispose();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

