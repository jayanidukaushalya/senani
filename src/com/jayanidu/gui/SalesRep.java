    private void loadTable() {
        try {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            ResultSet rs = MySQL.search("SELECT * FROM `sales_rep` INNER JOIN `branch` ON `sales_rep`.`branch_id` = `branch`.`id` INNER JOIN `company` ON `branch`.`company_id` = `company`.`id` WHERE `sales_rep`.`status` = 1");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("company.name"));
                v.add(rs.getString("branch.name"));
                v.add(rs.getString("sales_rep.name"));
                v.add(rs.getString("sales_rep.phone"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

