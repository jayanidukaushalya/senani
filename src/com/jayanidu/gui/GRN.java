    private void updateTotal() {

        double total = 0;

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            String rowTotal = jTable1.getValueAt(i, 11).toString();
            total = total + Double.parseDouble(rowTotal);
        }

        jLabel20.setText(new DecimalFormat("0.00").format(total));

    }

    private void loadPaymentMethods() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `pmethod`");
            Vector v = new Vector();
            v.add("Select Method");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel cbm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(cbm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

