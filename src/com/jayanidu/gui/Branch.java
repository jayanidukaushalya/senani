    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String cname = jComboBox1.getSelectedItem().toString();
        String bname = jTextField1.getText().trim();
        String l1 = jTextField2.getText().trim();
        String l2 = jTextField3.getText().trim();
        String cnum = jTextField4.getText().trim();
        String city = jComboBox3.getSelectedItem().toString();

        if (cname.equals("Select Company")) {
            JOptionPane.showMessageDialog(this, "Please select a company!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (bname.isBlank()) {
            JOptionPane.showMessageDialog(this, "Branch Name field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (bname.length() > 45) {
            JOptionPane.showMessageDialog(this, "Branch Name field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (l1.isBlank()) {
            JOptionPane.showMessageDialog(this, "Address Line 1 field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (l1.length() > 45) {
            JOptionPane.showMessageDialog(this, "Address Line 1 field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (l2.isBlank()) {
            JOptionPane.showMessageDialog(this, "Address Line 2 field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (l2.length() > 45) {
            JOptionPane.showMessageDialog(this, "Address Line 2 field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cnum.isBlank()) {
            JOptionPane.showMessageDialog(this, "Contact Number field empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cnum.length() > 45) {
            JOptionPane.showMessageDialog(this, "Contact Number field out of range!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select City")) {
            JOptionPane.showMessageDialog(this, "Please select a city!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet rs = MySQL.search("SELECT * FROM `branch` INNER JOIN `company` ON `branch`.`company_id` = `company`.`id` WHERE `branch`.`name` = '" + bname + "' AND `company`.`name` = '" + cname + "' AND `branch`.`status` = 1 AND `company`.`status` = 1");
                if (!rs.next()) {
                    ResultSet rs1 = MySQL.search("SELECT * FROM `company` WHERE `name` = '" + cname + "'");
                    ResultSet rs2 = MySQL.search("SELECT * FROM `city` WHERE `name` = '" + city + "'");
                    rs1.next();
                    rs2.next();

                    MySQL.iud("INSERT INTO `branch`(`company_id`,`name`,`phone`,`line1`,`line2`,`city_id`) VALUES(" + rs1.getInt("id") + ",'" + bname + "','" + cnum + "','" + l1 + "','" + l2 + "'," + rs2.getInt("id") + ")");
                    JOptionPane.showMessageDialog(this, "Branch added successfull!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    clearFields();
                    loadTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Branch already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

