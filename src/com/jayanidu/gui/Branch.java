    public void loadCompanies() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `company` WHERE `status` = 1 ORDER BY `name` ASC");
            Vector v = new Vector();
            v.add("Select Company");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel cbm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(cbm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

