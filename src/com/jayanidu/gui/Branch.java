    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 1) {
            int row = jTable1.getSelectedRow();

            if (row != -1) {
                try {

                    ResultSet rs = MySQL.search("SELECT * FROM `branch` INNER JOIN `company` ON `branch`.`company_id`  = `company`.`id` WHERE `branch`.`name` = '" + jTable1.getValueAt(row, 1).toString() + "' AND `company`.`name` = '" + jTable1.getValueAt(row, 0).toString() + "'");
                    rs.next();
                    clickedId = rs.getInt("branch.id");
                    clickedBname = rs.getString("branch.name");
                    clickedCnum = rs.getString("branch.phone");
                    clickedL1 = rs.getString("branch.line1");
                    clickedL2 = rs.getString("branch.line2");
                    jTextField1.setText(clickedBname);
                    jTextField2.setText(clickedL1);
                    jTextField3.setText(clickedL2);
                    jTextField4.setText(clickedCnum);

                    ResultSet rs1 = MySQL.search("SELECT * FROM `company` WHERE `id` = " + rs.getInt("branch.company_id") + "");
                    ResultSet rs2 = MySQL.search("SELECT * FROM `city` WHERE `id` = " + rs.getInt("branch.city_id") + "");
                    rs1.next();
                    rs2.next();

                    Vector v1 = new Vector();
                    clickedCname = rs1.getString("name");
                    v1.add(clickedCname);
                    ResultSet rs3 = MySQL.search("SELECT * FROM `company` WHERE `name` <> '" + clickedCname + "' AND `status` = 1 ORDER BY `name` ASC");
                    while (rs3.next()) {
                        v1.add(rs3.getString("name"));
                    }

                    DefaultComboBoxModel cbm1 = new DefaultComboBoxModel(v1);
                    jComboBox1.setModel(cbm1);

                    Vector v2 = new Vector();
                    clickedCity = rs2.getString("name");
                    v2.add(clickedCity);
                    ResultSet rs4 = MySQL.search("SELECT * FROM `city` WHERE `name` <> '" + clickedCity + "' AND `status` = 1 ORDER BY `name` ASC");
                    while (rs4.next()) {
                        v2.add(rs4.getString("name"));
                    }

                    DefaultComboBoxModel cbm2 = new DefaultComboBoxModel(v2);
                    jComboBox3.setModel(cbm2);

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }

        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();

            if (row != -1) {
                if (sr != null) {
                    sr.jTextField6.setText(clickedCname);
                    sr.jTextField5.setText(clickedBname);
                    sr.branchId = clickedId;
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

