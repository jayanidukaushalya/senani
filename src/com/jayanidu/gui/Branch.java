    private void loadTable() {
        try {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MySQL.search("SELECT * FROM `branch` INNER JOIN `company` ON `branch`.`company_id` = `company`.`id` INNER JOIN `city` ON `branch`.`city_id` = `city`.`id` WHERE `branch`.`status` = 1 ORDER BY `company`.`name` ASC");

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("company.name"));
                v.add(rs.getString("branch.name"));
                v.add(rs.getString("branch.phone"));
                v.add(rs.getString("branch.line1") + " " + rs.getString("branch.line2"));
                v.add(rs.getString("city.name"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

